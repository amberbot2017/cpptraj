#!/bin/bash

. ../MasterTest.sh

CleanFiles runavg.in runavg.crd R1.dat
CheckNetcdf
TOP="../tz2.parm7"
INPUT="runavg.in"

cat > $INPUT <<EOF
trajin ../tz2.nc
runavg window 5
rms R1 :1-13&!@H= out R1.dat
EOF
RunCpptraj "Coordinate Running Average test."
DoTest R1.dat.save R1.dat

NotParallel "Coordinate Running Average Traj Write"
if [[ $? -eq 0 ]] ; then
  cat > $INPUT <<EOF
trajin ../tz2.nc 1 14
runavg window 5
trajout runavg.crd nobox title "trajectory generated by ptraj"
EOF
  RunCpptraj "Coordinate Running Average Traj Write"
  DoTest runavg.crd.save runavg.crd
fi
EndTest

exit 0
